package Strings;

/*In Java, the toString() function is a method that is defined in the Object class and 
can be used to convert an object into a string. Since every class in Java is a subclass 
of Object, the toString() function is available to all Java classes. */

public class toString {
    public static void main(String[] args) {
        int number = 123;
        String str = Integer.toString(number);
        System.out.println(str);
    }
    
}
